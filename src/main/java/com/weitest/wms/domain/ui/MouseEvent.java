package com.weitest.wms.domain.ui;

public class MouseEvent {
    public Boolean altKey;
    public Long button;
    public Long clientX;
    public Long clientY;
    public Boolean ctrlKey;
    public Boolean metaKey;
    public Long movementX;
    public Long movementY;
    public Long offsetX;
    public Long offsetY;
    public Long pageX;
    public Long pageY;
    public Long screenX;
    public Long screenY;
    public Long which;

    public Boolean getAltKey() {
        return altKey;
    }

    public void setAltKey(Boolean altKey) {
        this.altKey = altKey;
    }

    public Long getButton() {
        return button;
    }

    public void setButton(Long button) {
        this.button = button;
    }

    public Long getClientX() {
        return clientX;
    }

    public void setClientX(Long clientX) {
        this.clientX = clientX;
    }

    public Long getClientY() {
        return clientY;
    }

    public void setClientY(Long clientY) {
        this.clientY = clientY;
    }

    public Boolean getCtrlKey() {
        return ctrlKey;
    }

    public void setCtrlKey(Boolean ctrlKey) {
        this.ctrlKey = ctrlKey;
    }

    public Boolean getMetaKey() {
        return metaKey;
    }

    public void setMetaKey(Boolean metaKey) {
        this.metaKey = metaKey;
    }

    public Long getMovementX() {
        return movementX;
    }

    public void setMovementX(Long movementX) {
        this.movementX = movementX;
    }

    public Long getMovementY() {
        return movementY;
    }

    public void setMovementY(Long movementY) {
        this.movementY = movementY;
    }

    public Long getOffsetX() {
        return offsetX;
    }

    public void setOffsetX(Long offsetX) {
        this.offsetX = offsetX;
    }

    public Long getOffsetY() {
        return offsetY;
    }

    public void setOffsetY(Long offsetY) {
        this.offsetY = offsetY;
    }

    public Long getPageX() {
        return pageX;
    }

    public void setPageX(Long pageX) {
        this.pageX = pageX;
    }

    public Long getPageY() {
        return pageY;
    }

    public void setPageY(Long pageY) {
        this.pageY = pageY;
    }

    public Long getScreenX() {
        return screenX;
    }

    public void setScreenX(Long screenX) {
        this.screenX = screenX;
    }

    public Long getScreenY() {
        return screenY;
    }

    public void setScreenY(Long screenY) {
        this.screenY = screenY;
    }

    public Long getWhich() {
        return which;
    }

    public void setWhich(Long which) {
        this.which = which;
    }
}
